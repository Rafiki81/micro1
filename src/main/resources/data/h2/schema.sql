DROP  TABLE PERSONAS 		IF EXISTS	CASCADE;	
DROP  TABLE CLIENTES 		IF EXISTS	CASCADE;
DROP  TABLE PRODUCTOS 		IF EXISTS	CASCADE;	
DROP  TABLE PEDIDOS 		IF EXISTS	CASCADE;
DROP  TABLE DETALLE_PEDIDOS IF EXISTS	CASCADE;

CREATE TABLE PERSONAS (
	DNI 			VARCHAR(10)		NOT NULL,
	NOMBRE			VARCHAR(50)		,
	APELLIDOS		VARCHAR(100)	,
	FECHA_ALTA		TIMESTAMP		,
	PRIMARY KEY (DNI)
);
CREATE TABLE CLIENTES( 
	DNI 			VARCHAR(10)		NOT NULL,
	TARJETA_GOLD    BOOLEAN			,
	PRIMARY KEY (DNI)				
);

CREATE TABLE PRODUCTOS( 
	CODIGO_PROVEEDOR		BIGINT			NOT NULL,
	CODIGO_PRODUCTO         BIGINT			NOT NULL,
	NOMBRE                  VARCHAR(100)	,
	PESO                    DOUBLE				,
	TIPO_PRODUCTO		 VARCHAR(20)		NOT NULL ,
	PRIMARY KEY (CODIGO_PROVEEDOR,CODIGO_PRODUCTO)
);

CREATE TABLE PEDIDOS	(  
	CODIGO_PEDIDO		BIGINT			NOT NULL,
	FECHA 				TIMESTAMP		,
	DIRECCION 			VARCHAR(100)	NOT NULL,
	POBLACION			VARCHAR(100)	NOT NULL,
	CODIGO_POSTAL		VARCHAR(10)		NOT NULL,
	PROVINCIA			VARCHAR(50)		NOT NULL,
	PAIS				VARCHAR(20)		NOT NULL,
	DNI					VARCHAR(10)		NOT NULL,
	TIPO_ENTREGA  		VARCHAR(20)		NOT NULL,
	PRIMARY KEY (CODIGO_PEDIDO)
);

CREATE TABLE DETALLE_PEDIDOS(
	CODIGO_DETALLE_PEDIDOS 	int			    NOT NULL,
	CODIGO_PEDIDO			BIGINT			NOT NULL,
	CODIGO_PROVEEDOR		BIGINT			NOT NULL,
	CODIGO_PRODUCTO     	BIGINT			NOT NULL,
	CANTIDAD				int				NOT NULL,
	PRIMARY KEY (CODIGO_DETALLE_PEDIDOS)
);